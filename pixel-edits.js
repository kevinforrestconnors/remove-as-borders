function rangeY(str) {
  const [x, y] = str.split(",");
  const [yStart, yEnd] = y.split("-").map(Number);
  const pixels = [];
  for (let i = yStart; i <= yEnd; i++) {
    pixels.push([Number(x), i]);
  }
  return pixels;
}

